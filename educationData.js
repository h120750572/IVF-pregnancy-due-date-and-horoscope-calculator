// 根據懷孕週數獲取衛教資訊
export function getEducationByWeeks(weeks) {
  // 找到最接近的週數資訊
  const availableWeeks = Object.keys(pregnancyEducationData).map(Number).sort((a, b) => a - b);
  
  let targetWeek = availableWeeks[0];
  for (let week of availableWeeks) {
    if (weeks >= week) {
      targetWeek = week;
    } else {
      break;
    }
  }
  
  return pregnancyEducationData[targetWeek] || null;
}

